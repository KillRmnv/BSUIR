"""Контроллер создания элементов UI главного окна."""

from PyQt6.QtWidgets import (
    QWidget,
    QHBoxLayout,
    QPushButton,
    QToolButton,
    QMenu,
    QTextEdit,
    QVBoxLayout,
)


class UIController:
    """Создаёт и настраивает элементы интерфейса."""

    def __init__(self, window):
        self.window = window

    def create_toolbar(self) -> QWidget:
        """Создает панель инструментов."""
        toolbar = QWidget()
        layout = QHBoxLayout(toolbar)

        btn_open = QPushButton("Открыть файл")
        btn_open.clicked.connect(self.window.open_and_load_file)
        layout.addWidget(btn_open)

        # Меню файловых операций
        btn_file_menu = QToolButton()
        btn_file_menu.setText("Файл ▼")
        btn_file_menu.setPopupMode(QToolButton.ToolButtonPopupMode.InstantPopup)

        file_menu = QMenu(btn_file_menu)
        file_menu.addAction("Импорт словаря", self.window.import_dictionary)
        file_menu.addAction("Добавить к словарю", self.window.stack_dictionary)
        file_menu.addSeparator()
        file_menu.addAction("Экспорт словаря", self.window.export_dictionary)
        file_menu.addSeparator()
        file_menu.addAction("Очистить словарь", self.window.clear_dictionary)

        btn_file_menu.setMenu(file_menu)
        layout.addWidget(btn_file_menu)

        layout.addStretch()
        return toolbar

    def create_terminology_tab(self) -> QWidget:
        """Создает вкладку с лингвистической терминологией."""
        widget = QWidget()
        layout = QVBoxLayout(widget)
        help_text = QTextEdit()
        help_text.setReadOnly(True)
        help_content = """
        <h1>Справочник лингвистических терминов</h1>

        <h2>Основные понятия</h2>

        <h3>Лемма</h3>
        <p><b>Лемма</b> — это словарная форма слова, которая выступает заголовком словарной статьи. 
        Для существительных это именительный падеж единственного числа, для глаголов — инфинитив 
        (неопределённая форма). Например: "run", "book", "beautiful".</p>
        <p><i>Пример:</i> слова "running", "ran", "runs" имеют одну лемму — "run".</p>

        <h3>Лексема</h3>
        <p><b>Лексема</b> — это единица словарного состава языка, совокупность всех словоформ, 
        имеющих общее лексическое значение и принадлежащих одной части речи. Лексема объединяет 
        все формы одного слова.</p>
        <p><i>Пример:</i> лексема БЕГАТЬ включает словоформы: бегать, бегаю, бегаешь, бегал, бегая и т.д.</p>

        <h3>Словоформа</h3>
        <p><b>Словоформа</b> — это конкретная форма слова, встречающаяся в тексте. Это реализация 
        лексемы в речи с определёнными грамматическими характеристиками.</p>
        <p><i>Пример:</i> в предложении "The cats are sleeping" словоформами являются: 
        "cats" (мн.ч.), "are" (3 л., мн.ч.), "sleeping" (причастие наст. вр.).</p>

        <h2>Части речи в системе</h2>

        <h3>NOUN (Существительное)</h3>
        <p>Имена существительные обозначают предметы, лица, животных, явления. В английском языке 
        не имеют падежных окончаний (кроме притяжательного).</p>
        <p><i>Примеры:</i> book, cat, love, freedom, John.</p>

        <h3>VERB (Глагол)</h3>
        <p>Глаголы обозначают действие или состояние. Могут изменяться по лицам, числам, временам, 
        наклонениям.</p>
        <p><i>Примеры:</i> run, be, have, think, become.</p>

        <h3>ADJ (Прилагательное)</h3>
        <p>Прилагательные обозначают признак предмета, отвечают на вопросы "какой?", "чей?". 
        Имеют степени сравнения.</p>
        <p><i>Примеры:</i> big, beautiful, interesting, my, this.</p>

        <h3>ADV (Наречие)</h3>
        <p>Наречия обозначают признак действия, качества или другого признака. Отвечают на вопросы 
        "как?", "где?", "когда?", "почему?".</p>
        <p><i>Примеры:</i> quickly, here, yesterday, very, well.</p>

        <h3>PROPN (Имя собственное)</h3>
        <p>Имена собственные — это названия конкретных людей, мест, организаций, праздников. 
        Пишутся с заглавной буквы.</p>
        <p><i>Примеры:</i> London, Microsoft, Christmas, Einstein.</p>

        <h2>Связанные понятия</h2>

        <h3>Словосочетание</h3>
        <p><b>Словосочетание</b> — это синтаксическая единица, состоящая из двух или более слов, 
        связанных между собой грамматически и семантически.</p>
        <p><i>Примеры:</i> "fast car", "run quickly", "beautiful day", "read a book".</p>

        <h3>Частота (Frequency)<extract_words/h3>
        <p><b>Частота</b> — количество вхождений словосочетания в обработанный текст. 
        Показывает, насколько часто встречается данное сочетание.</p>

        <h3>Синтез словосочетаний</h3>
        <p><b>Синтез</b> — процесс генерации новых словосочетаний на основе случайного сочетания 
        слов из морфологического словаря. Позволяет создавать грамматически возможные, но не 
        обязательно семантически осмысленные фразы.</p>
        """
        help_text.setHtml(help_content)
        layout.addWidget(help_text)
        return widget

    def create_help_tab(self) -> QWidget:
        """Создает вкладку помощи с памяткой по работе с программой."""
        widget = QWidget()
        layout = QVBoxLayout(widget)
        help_text = QTextEdit()
        help_text.setReadOnly(True)
        help_content = """
        <h2>Памятка по работе с программой</h2>

        <h3>Важно</h3>
        <p><b>База данных очищается при каждом запуске.</b> Для сохранения результатов 
        используйте «Экспорт словаря» в меню «Файл».</p>

        <h3>Вкладка «Загрузка файла»</h3>
        <p>1. Нажмите «Открыть файл» в панели инструментов или «Обзор...» на вкладке</p>
        <p>2. Выберите файл (TXT, RTF, PDF, DOC, DOCX) и нажмите «Загрузить файл»</p>
        <p>3. Текст появится в поле, его можно редактировать</p>
        <p>4. Нажмите «Обработать текст» — слова и словосочетания добавятся в словарь</p>

        <h3>Вкладка «Словарь»</h3>
        <p>Просмотр и управление словарем:</p>
        <ul>
        <li><b>Поиск</b> — фильтр по началу слова (в реальном времени)</li>
        <li><b>Часть речи</b> — фильтр по грамматической категории</li>
        <li><b>Кол-во сочетаний</b> — фильтр по точному количеству словосочетаний</li>
        <li>Выберите слово для просмотра его словосочетаний</li>
        <li>Двойной клик по ячейке позволяет редактировать</li>
        </ul>

        <h3>Вкладка «Синтез»</h3>
        <p>Генерация новых фраз из случайных слов:</p>
        <p>1. Загрузите слова: «Загрузить из текущего текста» или «Загрузить из словаря»</p>
        <p>2. Нажмите «Синтезировать словосочетание»</p>

        <h3>Вкладка «Тестирование»</h3>
        <p>Запустите тесты для измерения производительности операций.</p>

        <h3>Файловые операции (меню «Файл»)</h3>
        <ul>
        <li><b>Импорт словаря</b> — загружает словарь из файла (заменяет текущий)</li>
        <li><b>Добавить к словарю</b> — добавляет словарь из файла к текущему</li>
        <li><b>Экспорт словаря</b> — сохраняет словарь в файл (TXT, CSV)</li>
        <li><b>Очистить словарь</b> — удаляет все слова и словосочетания</li>
        </ul>
        """
        help_text.setHtml(help_content)
        layout.addWidget(help_text)
        return widget
